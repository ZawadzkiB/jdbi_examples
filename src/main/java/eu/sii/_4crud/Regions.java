package eu.sii._4crud;

public class Regions {

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public Regions setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public Regions setName(String name) {
        this.name = name;
        return this;
    }
}
